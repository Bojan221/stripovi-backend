const mongoose = require("mongoose");

const PublisherSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    country: {
      type: String,
      required: true,
    },
    createdBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  { timestamps: true },
);

const Publisher = mongoose.model("Publisher", PublisherSchema);
module.exports = Publisher;
